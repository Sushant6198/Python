def Information(Name, Age, Salary):
    print("Name is : ",Name)
    print("Age is : ",Age)
    print("Salary is : ",Salary)

Information("Amit",32,90000)

Information("Pooja",29,70000)

Information(Age = 21,Name = "Sagar",Salary = 60000)
